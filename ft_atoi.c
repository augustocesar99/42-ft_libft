#include "libft.h"

int	ft_atoi(const char *str);

int	ft_atoi(const char *str)
{
	int	result;
	int	signal;

	while (*str == ' ')
		str++;
	result = 0;
	signal = 1;
	if (*str == '-')
	{
		signal = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + (*str - '0');
		str++;
	}
	return (signal * result);
}
